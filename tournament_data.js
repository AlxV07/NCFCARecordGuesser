export function parse_raw_tournament_data() {
    /*
    Returns: `[teamtoid, idtoteam, rounds, rooms]`
     */
    const determiner_lines = RawTournamentData.raw_round1.trim().split('\n')

    // Determine `is_bye` and `rooms`
    const is_bye = determiner_lines.length % 3 !== 0;
    const rooms = Math.floor(determiner_lines.length / 3)

    // Assign Team Ids
    let idCounter = 0
    const teamtoid = {}
    const idtoteam = {}
    let i = 0; while (i < rooms*3 + is_bye*2) {
        const room = determiner_lines[i++].trim()
        if (room === 'BYE') {
            let t1 = determiner_lines[i++].trim();
            const id = idCounter++
            teamtoid[t1] = id
            idtoteam[id] = t1
        } else {
            let t1 = determiner_lines[i++].trim(); t1 = t1.substring(0, t1.length - 6)
            const id1 = idCounter++;
            teamtoid[t1] = id1
            idtoteam[id1] = t1

            let t2 = determiner_lines[i++].trim(); t2 = t2.substring(0, t2.length - 6)
            const id2 = idCounter++;
            teamtoid[t2] = id2
            idtoteam[id2] = t2
        }
    }

    // Build Rounds
    const raw_rounds = [
        RawTournamentData.raw_round1,
        RawTournamentData.raw_round2,
        RawTournamentData.raw_round3,
        RawTournamentData.raw_round4,
        RawTournamentData.raw_round5,
        RawTournamentData.raw_round6]
    let rounds_posted = 0
    for (const raw_round of raw_rounds) {
        if (raw_round.trim().length === 0) {
            break
        }
        rounds_posted += 1
    }
    /* `rounds` dict structure:
    rounds = {
     round: {
        roomid: [t1id, t2id]
        ...
     }
     ...
    }
     */
    const rounds = {
        0: null,
        1: null,
        2: null,
        3: null,
        4: null,
        5: null,
    }
    for (let round = 0; round < rounds_posted; round++) {
        const r = {}
        rounds[round] = r
        const raw_round_lines = raw_rounds[round].trim().split('\n')
        let roomid = 0
        let i = 0; while (i < rooms*3 + is_bye*2) {
            const room = raw_round_lines[i++].trim()
            if (room === 'BYE') {
                let t1 = raw_round_lines[i++].trim();
                r['BYE'] = [teamtoid[t1]]
            } else {
                let t1 = raw_round_lines[i++].trim(); t1 = t1.substring(0, t1.length - 6)
                let t2 = raw_round_lines[i++].trim(); t2 = t2.substring(0, t2.length - 6)
                r[roomid++] = [teamtoid[t1], teamtoid[t2]]
            }
        }
    }
    return [teamtoid, idtoteam, rounds, rooms]
}
